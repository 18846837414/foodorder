using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using Data;
using Models;
using Data.Services;
using FoodOrder.Models.CategoriesViewModel;

namespace FoodOrder.Controllers
{
    public class CategoriesController : Controller
    {
        private readonly ApplicationDBContext _context;
        private readonly IOrderServices _orderServices;

        public CategoriesController(ApplicationDBContext context, IOrderServices orderServices)
        {
            _context = context;
            _orderServices = orderServices;
        }

        // GET: Categories
        public async Task<IActionResult> Index()
        {
            var vm = new IndexViewModel()
            {
                Categories = await _orderServices.GetAllCategoriesAsync()
            };
            
            return View(vm.Categories);
        }

        // GET: Categories/Details/5
        public async Task<IActionResult> Details(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var category = await _context.Categories
                .SingleOrDefaultAsync(m => m.Id == id);
            if (category == null)
            {
                return NotFound();
            }

            return View(category);
        }

        // GET: Categories/Create
        public IActionResult Create()
        {
            return View();
        }

        // POST: Categories/Create
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for 
        // more details see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Create([Bind("Id,DisplayOrder,Name")] Category category)
        {
            if (ModelState.IsValid)
            {
                await _orderServices.CreateCategoryAsync(category);
                
                return RedirectToAction(nameof(Index));
            }
            return View(category);
        }

        // GET: Categories/Edit/5
        public async Task<IActionResult> Edit(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }
            var category = await _orderServices.GetCategoryAsync(id);
            
            
            if (category == null)
            {
                return NotFound();
            }
            return View(category);
        }

        // POST: Categories/Edit/5
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for 
        // more details see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Edit(int id, [Bind("Id,DisplayOrder,Name")] Category category)
        {
            if (id != category.Id)
            {
                return NotFound();
            }

            if (ModelState.IsValid)
            {
                try
                {
                    category = await _orderServices.EditCategoryAsync(id);
                  
                }
                catch (DbUpdateConcurrencyException)
                {
                    if (! await _orderServices.CategoryExists(category.Id))
                    {
                        return NotFound();
                    }
                    else
                    {
                        throw;
                    }
                }
                return RedirectToAction(nameof(Index));
            }
            return View(category);
        }

        // GET: Categories/Delete/5
        public async Task<IActionResult> Delete(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            
            var category = await _orderServices.GetCategoryAsync(id);

            if (category == null)
            {
                return NotFound();
            }

            return View(category);
        }

        // POST: Categories/Delete/5
        [HttpPost, ActionName("Delete")]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> DeleteConfirmed(int id)
        {
            await _orderServices.DeleteCategoryAsync(id);
            return RedirectToAction(nameof(Index));
        }

       
    }
}
